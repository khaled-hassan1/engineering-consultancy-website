        // Convert to LTR classes
        el.className = el.className
          .replace(/\btext-end\b/g, 'text-start')
          .replace(/\btext-md-end\b/g, 'text-md-start')
          .replace(/\btext-lg-end\b/g, 'text-lg-start')
